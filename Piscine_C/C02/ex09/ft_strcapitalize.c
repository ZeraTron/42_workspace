char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[0] >= 'a' && str[0] <= 'z')
		{
			str[0] = str[0] - 32;
		}
		if ((str[i] >= 'a' && str[i] <= 'z') && (str[(i - 1)] == ' '))
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}
