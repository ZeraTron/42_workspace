int	ft_atoi(char *str)
{
	int i;
	int x;
	int res;

	i = 0;
	res = 0;
	while (str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (10 * res) + str[i] - '0';
		i++;
	}
	if (x % 2 == 1)
		return (-res);
	return (res);
}
